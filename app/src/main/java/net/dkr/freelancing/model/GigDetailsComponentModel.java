package net.dkr.freelancing.model;

public class GigDetailsComponentModel {

    String name;
   boolean status;

    public GigDetailsComponentModel(String name, boolean status) {
        this.name = name;
        this.status = status;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public boolean isStatus() {
        return status;
    }

    public void setStatus(boolean status) {
        this.status = status;
    }
}
